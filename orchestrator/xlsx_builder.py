def build_tm(m6_output: str, output_path: str, version: str = "1.0") -> str:
    """Build TM-0xx.xlsx from M6 traceability output."""
    wb = openpyxl.Workbook()
    _add_cover_sheet(wb, "Traceability Matrix", "TM-0xx",
                     version, "M6", "Phi-4 14B")

    parsed = _parse_traceability(m6_output)

    # Sheet 1 — Forward traceability
    ws_fwd = wb.create_sheet("Forward Traceability")
    fwd_headers = ['PDS-ID', 'HLR-ID', 'LLR-ID', 'Code Function',
                   'LLT-ID (SUTC)', 'HLT-ID (SVTC)', 'Status']
    fwd_widths   = [15, 20, 20, 30, 20, 20, 15]

    for col, (h, w) in enumerate(zip(fwd_headers, fwd_widths), 1):
        ws_fwd.cell(row=1, column=col, value=h)
        ws_fwd.column_dimensions[get_column_letter(col)].width = w
    _style_header_row(ws_fwd, 1, len(fwd_headers))

    for i, row in enumerate(parsed['forward'], 2):
        vals = [row.get('PDS-ID',''), row.get('HLR-ID',''), row.get('LLR-ID',''),
                row.get('Code',''), row.get('LLT-ID',''), row.get('HLT-ID',''),
                row.get('Status','')]
        for col, val in enumerate(vals, 1):
            cell = ws_fwd.cell(row=i, column=col, value=val)
            cell.border = THIN_BORDER
            cell.font = BODY_FONT
            if vals[-1] == 'GAP':
                cell.fill = CORAL_FILL
            elif vals[-1] == 'COMPLETE':
                cell.fill = GREEN_FILL
            else:
                cell.fill = GRAY_FILL if i % 2 == 0 else WHITE_FILL

    # Sheet 2 — Raw M6 output (full text for review)
    ws_raw = wb.create_sheet("Full M6 Output")
    ws_raw.column_dimensions['A'].width = 150
    for i, line in enumerate(m6_output.split('\n')[:500], 1):
        ws_raw.cell(row=i, column=1, value=line).font = Font(name="Courier New", size=9)

    # Sheet 3 — Gaps
    ws_gaps = wb.create_sheet("Traceability Gaps")
    ws_gaps.cell(row=1, column=1, value="Traceability Gap").font = WHITE_FONT
    ws_gaps.cell(row=1, column=1).fill = BLUE_FILL
    ws_gaps.column_dimensions['A'].width = 100
    for i, gap in enumerate(parsed['gaps'], 2):
        ws_gaps.cell(row=i, column=1, value=gap)
        ws_gaps.cell(row=i, column=1).fill = CORAL_FILL
        ws_gaps.cell(row=i, column=1).font = BODY_FONT

    # Sheet 4 — Summary
    ws_sum = wb.create_sheet("Coverage Summary")
    summary_data = [
        ("Metric", "Value"),
        ("Total forward trace rows", len(parsed['forward'])),
        ("COMPLETE links", sum(1 for r in parsed['forward'] if r.get('Status') == 'COMPLETE')),
        ("PARTIAL links", sum(1 for r in parsed['forward'] if r.get('Status') == 'PARTIAL')),
        ("GAP links", sum(1 for r in parsed['forward'] if r.get('Status') == 'GAP')),
        ("Traceability gaps found", len(parsed['gaps'])),
        ("Certification readiness", parsed.get('summary', 'See Full M6 Output sheet')),
    ]
    for i, (k, v) in enumerate(summary_data, 1):
        ws_sum.cell(row=i, column=1, value=k).font = BOLD_FONT
        ws_sum.cell(row=i, column=1).fill = BLUE_FILL if i == 1 else GRAY_FILL
        ws_sum.cell(row=i, column=1).font = WHITE_FONT if i == 1 else BOLD_FONT
        ws_sum.cell(row=i, column=2, value=str(v)).font = BODY_FONT
        ws_sum.column_dimensions['A'].width = 35
        ws_sum.column_dimensions['B'].width = 60

    os.makedirs(os.path.dirname(output_path) or '.', exist_ok=True)
    wb.save(output_path)
    return output_path
